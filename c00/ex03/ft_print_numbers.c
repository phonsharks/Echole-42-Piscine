#include <unistd.h>

void	ft_putchar(char a)
{
	write(1, &a, 1);
}

void	ft_print_numbers(void)
{
	char	s;

	s = '0';
	while (s <= '9')
	{
		ft_putchar(s);
		s++;
	}
}
